    public void cargarDatosTabla(){
        DefaultTableModel dtm=new DefaultTableModel();
        dtm.addColumn("Patente");
        dtm.addColumn("Marca");
        dtm.addColumn("Modelo");
        dtm.addColumn("Año");
        dtm.addColumn("Combustible");
        
        for (Vehiculo recorrer: listaVehiculo){
        dtm.addRow(new Object [] {recorrer.getPatente(),recorrer.getMarca(),recorrer.getModelo(),recorrer.getAnio(),recorrer.getCombustible()});
        }
        this.tabla_registros.setModel(dtm);
    }
    
