    private void btn_guardarVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarVehiculoActionPerformed
        String patente, marca, modelo, combustible;
        int anio;
        patente = this.txt_patente.getText();
        marca = this.txt_marca.getText();
        modelo = this.txt_modelo.getText();
        anio = Integer.parseInt(this.txt_anio.getText());
        combustible = this.txt_combustible.getText();
        
        Vehiculo v=new Vehiculo(patente, marca, modelo, anio, combustible);
        listaVehiculo.add(v);
        cargarDatosTabla();
        limpiarCajas();
        JOptionPane.showMessageDialog(null,v.infoVehiculo());
        
        
    }//GEN-LAST:event_btn_guardarVehiculoActionPerformed

